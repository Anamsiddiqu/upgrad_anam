class AddNumbers {
    public static void main(String[] args) {
        // Declaring variable
        float num1 = 5;
        float num2 = 10;
        // Adding Numbers
        float sum = num1 + num2;
        System.out.println("Sum: " + sum);
        // Substracting Numbers
        float sub = num1 - num2 ;
        System.out.println("sub: " + sub);
        // Dividing Numbers
        float Div = num1 / num2 ;
        System.out.println("Div: " + Div);
    }
}